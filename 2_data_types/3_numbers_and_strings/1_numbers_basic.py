# Basic Number Operations in Python
# Python Version: Python has supported basic number types (int, float) since version 1.0.

# Python supports both integer (int) and floating-point (float) numbers, along with a variety of operations.

# Addition, subtraction, multiplication, division
a = 10
b = 3
print(f"a + b = {a + b}")  # Outputs: a + b = 13
print(f"a - b = {a - b}")  # Outputs: a - b = 7
print(f"a * b = {a * b}")  # Outputs: a * b = 30
print(f"a / b = {a / b}")  # Outputs: a / b = 3.3333333333333335

# Floor division (//) and modulus (%)
print(f"a // b = {a // b}")  # Outputs: a // b = 3 (integer division)
print(f"a % b = {a % b}")  # Outputs: a % b = 1 (remainder)

# Exponentiation
print(f"a ** b = {a ** b}")  # Outputs: a ** b = 1000 (10 raised to the power of 3)

# Note: Python's numeric operations are highly versatile and support complex arithmetic, including floating-point arithmetic and large integers.
