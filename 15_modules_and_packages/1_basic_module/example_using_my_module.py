# example_using_my_module.py
# Python Version: Importing custom modules has been a core feature of
# Python since version 1.0.

import my_module

# Using a function from the custom module
result = my_module.my_function()
print(result)  # Outputs: Hello from my_function!

# Using a variable from the custom module
print(my_module.my_variable)  # Outputs: This is a variable in my_module.
