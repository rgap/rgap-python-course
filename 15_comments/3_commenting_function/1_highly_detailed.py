# Example 1: Highly Detailed (Stating the Obvious)


def add_numbers(a, b):
    # This function adds two numbers.
    # The first parameter 'a' is an integer or float.
    # The second parameter 'b' is an integer or float.
    # The function returns the sum of 'a' and 'b'.

    # Adding the two numbers
    result = a + b

    # Return the result of the addition
    return result


# Call the function with 5 and 3 and print the result
print(add_numbers(5, 3))  # Output should be 8
