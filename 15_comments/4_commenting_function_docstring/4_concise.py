def add_numbers(a, b):
    """
    Add two numbers.

    Parameters:
    a (int or float): First number.
    b (int or float): Second number.

    Returns:
    int or float: Sum of 'a' and 'b'.
    """
    return a + b


print(add_numbers(5, 3))  # Expected result: 8
