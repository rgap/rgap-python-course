def add_numbers(a, b):
    """
    Returns the sum of two numbers.

    Parameters:
    a (int or float): The first number.
    b (int or float): The second number.

    Returns:
    int or float: The sum of 'a' and 'b'.

    Useful for simple arithmetic operations.
    """
    return a + b


print(add_numbers(5, 3))
