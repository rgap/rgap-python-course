# Python Version: Introduced in Python 1.0 (January 1994)

# The print() function is used to output data to the console.
# It can take multiple arguments, which are separated by spaces in the output.

# In Python, strings can be enclosed in either single quotes ('') or double quotes ("").
# The print() function automatically adds a newline at the end of the output, so each call to print() will be on a new line.
print("Hello, World!")  # Outputs: Hello, World!

# You can print multiple arguments separated by commas.
print("Python", "is", "fun!")  # Outputs: Python is fun!

# Expressions inside print()
print("The sum of 2 + 2 is:", 2 + 2)  # Outputs: The sum of 2 + 2 is: 4
