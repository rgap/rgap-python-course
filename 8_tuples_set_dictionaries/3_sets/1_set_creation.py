# Set Creation in Python

# Creating a simple set
fruits_set = {"apple", "banana", "cherry"}
print(f"Fruits set: {fruits_set}")  # Outputs: Fruits set: {'apple', 'banana', 'cherry'}

# Creating an empty set
empty_set = set()
print(f"Empty set: {empty_set}")  # Outputs: Empty set: set()

# Creating a set using the set() constructor with a list
numbers_set = set([1, 2, 3, 3, 4])
print(f"Numbers set: {numbers_set}")  # Outputs: Numbers set: {1, 2, 3, 4}

# Note: Sets automatically remove duplicates and do not maintain order.
