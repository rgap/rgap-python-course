# Python Version: Lists (the Python equivalent of arrays) have been a part of Python since version 1.0 (January 1994).

# Array Creation in Python
# In Python, the equivalent of arrays in other languages is the list data structure.
# Lists are versatile, allowing you to store collections of items in a single variable.

# Creating a simple list (array) of numbers
numbers = [1, 2, 3, 4, 5]
print(f"Numbers: {numbers}")  # Outputs: Numbers: [1, 2, 3, 4, 5]

# Lists can contain different data types
mixed_list = [1, "two", 3.0, True]
print(f"Mixed list: {mixed_list}")  # Outputs: Mixed list: [1, 'two', 3.0, True]

# Lists can also be empty and filled later
empty_list = []
print(f"Empty list: {empty_list}")  # Outputs: Empty list: []

# Note: Lists in Python are dynamic, meaning you can change their size and contents during program execution.
# This makes them one of the most flexible data structures in Python.
