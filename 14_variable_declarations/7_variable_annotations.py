# Variable annotations (type hints)
# Python 3.6 introduced the ability to provide type hints for variables.

age: int = 25
name: str = "Alice"
is_student: bool = True

print(f"age (int): {age}")  # Outputs: age (int): 25
print(f"name (str): {name}")  # Outputs: name (str): Alice
print(f"is_student (bool): {is_student}")  # Outputs: is_student (bool): True
