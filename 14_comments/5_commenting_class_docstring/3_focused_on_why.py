# Example 3: Focused on the "Why" (Minimal Stating of the Obvious)


class Calculator:
    """
    Simple arithmetic operations class.
    """

    def add(self, a, b):
        """Return the sum of two numbers."""
        return a + b

    def subtract(self, a, b):
        """Return the difference of two numbers."""
        return a - b


# Example usage
calc = Calculator()
print(calc.add(5, 3))
