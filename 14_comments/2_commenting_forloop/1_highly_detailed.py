# Example 1: Highly Detailed (Stating the Obvious)

# Create an empty list to store squares
squares = []
# Loop through numbers 0 to 9
for x in range(10):
    # Calculate the square of x
    square = x**2
    # Add the square to the list
    squares.append(square)

# Print the list of squares
print(squares)  # Output should be [0, 1, 4, 9, 16, 25, 36, 49, 64, 81]
