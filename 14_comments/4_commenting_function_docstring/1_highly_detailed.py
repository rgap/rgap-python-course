def add_numbers(a, b):
    """
    This function adds two numbers together.

    Parameters:
    a (int or float): The first number to be added.
    b (int or float): The second number to be added.

    Returns:
    int or float: The sum of 'a' and 'b'.

    This function takes two numerical inputs and returns their sum.
    The addition is performed using the '+' operator.
    """

    result = a + b  # Perform the addition of the two numbers

    return result  # Return the result of the addition


print(add_numbers(5, 3))  # Call the function and print the result, which should be 8
