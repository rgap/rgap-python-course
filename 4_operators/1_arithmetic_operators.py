# Arithmetic Operators in Python
# Python Version: Arithmetic operators have been part of Python since version 1.0 (January 1994).

# Python supports standard arithmetic operations: addition, subtraction, multiplication, division, floor division, modulus, and exponentiation.

# Addition (+)
a = 10
b = 5
print(f"a + b = {a + b}")  # Outputs: a + b = 15

# Subtraction (-)
print(f"a - b = {a - b}")  # Outputs: a - b = 5

# Multiplication (*)
print(f"a * b = {a * b}")  # Outputs: a * b = 50

# Division (/)
print(f"a / b = {a / b}")  # Outputs: a / b = 2.0

# Floor Division (//)
print(f"a // b = {a // b}")  # Outputs: a // b = 2

# Modulus (%)
print(f"a % b = {a % b}")  # Outputs: a % b = 0

# Exponentiation (**)
print(f"a ** b = {a ** b}")  # Outputs: a ** b = 100000

# Note: Python's arithmetic operators support both integer and floating-point numbers, and they follow standard operator precedence rules.
