# Conditional Operator in Python (Ternary Operator)
# Python Version: The conditional operator (also known as the ternary operator) has been available since Python 2.5 (September 2006).

# The conditional operator allows you to return a value based on a condition in a single line of code.

# Syntax: value_if_true if condition else value_if_false

x = 10
y = 20

# Conditional operator example
if x > y:
    max_value = x
else:
    max_value = y
print(f"The maximum value is: {max_value}")  # Outputs: The maximum value is: 20

# Note: The conditional operator provides a concise way to write simple if-else statements.
