# Assignment Operators in Python
# Python Version: Assignment operators have been part of Python since version 1.0 (January 1994).

# Assignment operators are used to assign values to variables, often combining an operation with the assignment.

# Simple assignment (=)
x = 10
print(f"x = 10: {x}")  # Outputs: x = 10

# Add and assign (+=)
x += 5  # Equivalent to: x = x + 5
print(f"x += 5: {x}")  # Outputs: x += 5: 15

# Subtract and assign (-=)
x -= 3  # Equivalent to: x = x - 3
print(f"x -= 3: {x}")  # Outputs: x -= 3: 12

# Multiply and assign (*=)
x *= 2  # Equivalent to: x = x * 2
print(f"x *= 2: {x}")  # Outputs: x *= 2: 24

# Divide and assign (/=)
x /= 4  # Equivalent to: x = x / 4
print(f"x /= 4: {x}")  # Outputs: x /= 4: 6.0

# Floor divide and assign (//=)
x //= 2  # Equivalent to: x = x // 2
print(f"x //= 2: {x}")  # Outputs: x //= 2: 3.0

# Modulus and assign (%=)
x %= 2  # Equivalent to: x = x % 2
print(f"x %= 2: {x}")  # Outputs: x %= 2: 1.0

# Exponentiate and assign (**=)
x **= 3  # Equivalent to: x = x ** 3
print(f"x **= 3: {x}")  # Outputs: x **= 3: 1.0

# Note: Assignment operators are shorthand for performing operations and assigning the result to the same variable.
