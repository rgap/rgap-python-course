# Logical Operators in Python
# Python Version: Logical operators have been part of Python since version 1.0 (January 1994).

# Logical operators allow you to combine multiple conditions and return a boolean result.

# and - Returns True if both operands are true
print(f"True and False: {True and False}")  # Outputs: False

# or - Returns True if at least one operand is true
print(f"True or False: {True or False}")  # Outputs: True

# not - Inverts the boolean value
print(f"not True: {not True}")  # Outputs: False

# Note: Logical operators are essential in control flow, enabling more complex decision-making in conditions.
